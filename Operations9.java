public class Operations9 {
    public static void main(String[] args) {
       int number1= 100;
       int number2 = 200;
       System.out.println(number1 > number2);//false
       System.out.println(number1 >= number2);//false
       System.out.println(number1 < number2);//true
       System.out.println(number1 <= number2);//true
       System.out.println(number1 == number2);//false
       System.out.println(number1 != number2);//true
        
    }
}