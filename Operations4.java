public class Operations4{
    public static void main(String[] args) {
        System.out.println('A'+'A');//130
        System.out.println("A"+"A");//AA
        System.out.println(10+30+"A"+"A"+50+60);//40AA5060
        System.out.println(10+"A");//10A
        System.out.println(10+30+"A"+(10+30));//40A40
        System.out.println('D'-'A');//3
        System.out.println('D'/'A');//1
        System.out.println('D'%'A');//3
        System.out.println("A"+'A'+65+'B');//AA65B
       
        

    }
}